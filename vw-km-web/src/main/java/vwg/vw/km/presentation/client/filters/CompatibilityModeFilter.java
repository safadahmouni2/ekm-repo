public class CompatibilityModeFilter implements Filter {

	private final Log log = LogManager.get().getLog(CompatibilityModeFilter.class);

	/**
	 * (non-Javadoc)
	 * 
	 * @see javax.servlet.Filter#init(javax.servlet.FilterConfig)
	 */
	public void init(FilterConfig arg0) throws ServletException {
		log.info("VKM Application compatibility Mode  filter started");
	}

	/**
	 * (non-Javadoc)
	 * 
	 * @see javax.servlet.Filter#destroy()
	 */
	public void destroy() {
	}

	/**
	 * (non-Javadoc)
	 * 
	 * @see javax.servlet.Filter#doFilter(javax.servlet.ServletRequest, javax.servlet.ServletResponse,
	 *      javax.servlet.FilterChain)
	 */
	public void doFilter(ServletRequest req, ServletResponse res, FilterChain chain)
			throws IOException, ServletException {
		HttpServletRequest httpReq = (HttpServletRequest) req;
		HttpServletResponse httpResp = (HttpServletResponse) res;

		if (httpReq.getRequestURI().indexOf("javax.faces.resource/") < 0) {
			httpResp.addHeader("X-UA-Compatible", "IE=Edge");

		}
		chain.doFilter(req, res);
	}

}
